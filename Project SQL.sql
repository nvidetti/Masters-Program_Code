--Procedure to populate ACC_SEASON_ID in HEISMAN_WINNER
GO
CREATE OR ALTER PROCEDURE HEISMAN_WINNER_ACC_SEASON_ID AS
BEGIN
	DECLARE @loop INT = 1
    DECLARE @loopend INT
	DECLARE @ACC_SEASON_ID INT
	DECLARE @ACC_SCHOOL_ID INT
	DECLARE @HEISMAN_YEAR DATE
	SELECT @loopend = COUNT(*) FROM HEISMAN_WINNER
	WHILE @loop <= @loopend
	BEGIN
		SELECT 
			@ACC_SEASON_ID = ACCSEA.ACC_SEASON_ID,
			@ACC_SCHOOL_ID = ACCSEA.ACC_SCHOOL_ID,
			@HEISMAN_YEAR = HWIN.HEISMAN_YEAR
		FROM 
			ACC_SEASON ACCSEA
		INNER JOIN
			HEISMAN_WINNER HWIN
				ON ACCSEA.ACC_SCHOOL_ID = HWIN.ACC_SCHOOL_ID
				AND ACCSEA.SEASON_YEAR = HWIN.HEISMAN_YEAR
				AND HWIN.ACC_SEASON_ID IS NULL
		UPDATE HEISMAN_WINNER SET ACC_SEASON_ID = @ACC_SEASON_ID WHERE ACC_SCHOOL_ID = @ACC_SCHOOL_ID AND HEISMAN_YEAR = @HEISMAN_YEAR
		SELECT @loop = @loop + 1
	END
END
GO

