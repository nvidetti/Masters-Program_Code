CREATE OR ALTER VIEW ACC_SEASONS_VIEW AS
SELECT
	YEAR(SEASON.SEASON_YEAR) Season,
	SCHOOL.SCHOOL_NAME School,
	SEASON.WINS Wins,
	SEASON.LOSSES Losses,
	SEASON.TIES Ties,
	CAST((SEASON.WINS + (0.5*ISNULL(SEASON.TIES,0))) / (SEASON.WINS + SEASON.LOSSES + ISNULL(SEASON.TIES,0)) AS DECIMAL(4,3)) Win_Percentage,
	CASE SEASON.ACC_CHAMPION WHEN 1 THEN 'ACC Champion' WHEN 0 THEN '' END ACC_Champion_Flag,
	CASE WHEN HEISMAN.HEISMAN_WINNER_ID IS NOT NULL THEN HEISMAN.NAME + ' (' + HEISMAN.POSITION + ')' ELSE '' END Heisman_Flag
FROM
	ACC_SEASON SEASON
LEFT JOIN
	ACC_SCHOOL SCHOOL
		ON SEASON.ACC_SCHOOL_ID = SCHOOL.ACC_SCHOOL_ID
LEFT JOIN
	HEISMAN_WINNER HEISMAN
		ON SEASON.ACC_SEASON_ID = HEISMAN.ACC_SEASON_ID
GO

