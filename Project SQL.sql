CREATE OR ALTER PROCEDURE ENTER_SEASON
(@SCHOOL_NAME VARCHAR(50),@SEASON_YEAR DATE,@WINS INT,@LOSSES INT,@TIES INT,@ACC_CHAMPION BIT,
@NEW_SCHOOL_CITY VARCHAR(50) = NULL,@NEW_SCHOOL_STATE VARCHAR(2) = NULL,@NEW_GRADUATION_RATE DECIMAL(4,3) = NULL) AS
BEGIN
	IF NOT EXISTS (SELECT * FROM ACC_SCHOOL WHERE SCHOOL_NAME = @SCHOOL_NAME)
	BEGIN
		INSERT INTO ACC_SCHOOL (SCHOOL_NAME, SCHOOL_CITY, SCHOOL_STATE, GRADUATION_RATE)
			VALUES (@SCHOOL_NAME,@NEW_SCHOOL_CITY,@NEW_SCHOOL_STATE,@NEW_GRADUATION_RATE)
	END
	INSERT INTO ACC_SEASON (ACC_SCHOOL_ID,SEASON_YEAR,WINS,LOSSES,TIES,ACC_CHAMPION)
		VALUES ((SELECT ACC_SCHOOL_ID FROM ACC_SCHOOL WHERE SCHOOL_NAME = @SCHOOL_NAME),@SEASON_YEAR,@WINS,@LOSSES,@TIES,@ACC_CHAMPION)
	EXEC HEISMAN_WINNER_ACC_SCHOOL_ID
	EXEC HEISMAN_WINNER_ACC_SEASON_ID
END
