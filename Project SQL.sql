--What school has had the most wins in ACC history?
SELECT 
	*
FROM
	ACC_SCHOOLS_VIEW
WHERE
	ACC_Wins = (SELECT MAX(ACC_Wins) FROM ACC_SCHOOLS_VIEW)


--What school has had the most Heisman trophies won by an ACC player?
SELECT * FROM ACC_SCHOOLS_VIEW WHERE ACC_Heisman_Winners = (SELECT MAX(ACC_Heisman_Winners) FROM ACC_SCHOOLS_VIEW)
SELECT * FROM ACC_SCHOOLS_VIEW WHERE Total_Heisman_Trophy_Winners = (SELECT MAX(Total_Heisman_Trophy_Winners) FROM ACC_SCHOOLS_VIEW)


--Assuming a given student-athlete is from West Virginia, Which schools are in West Virginia or in a state bordering West Virginia?
--(West Virginia, Maryland, Virginia, Kentucky, Ohio, or Pennsylvania)
SELECT
	*
FROM
	ACC_SCHOOLS_VIEW
WHERE
	State IN ('WV','MD','VA','KY','OH','PA')

--What school has had the most undefeated (0 loss) seasons?
SELECT
	School,
	COUNT(CASE WHEN Losses = 0 THEN 1 END) Undefeated_Seasons
FROM
	ACC_SEASONS_VIEW
GROUP BY
	School
HAVING
	COUNT(CASE WHEN Losses = 0 THEN 1 END) = (SELECT TOP 1 COUNT(CASE WHEN Losses = 0 THEN 1 END) Undefeated_Seasons
													FROM ACC_SEASONS_VIEW GROUP BY School ORDER BY Undefeated_Seasons DESC)

--Are there any schools in the top 5 for all three of the following metrics?
	--ACC Championships
	--Heisman trophies won by an ACC player
	--Graduation rate

--TOP 5 ACC_Championships
SELECT TOP 5 ACC_Championships FROM (SELECT DISTINCT ACC_Championships FROM ACC_SCHOOLS_VIEW)SUBQUERY ORDER BY ACC_Championships DESC

--TOP 5 ACC_Heisman_Winners
SELECT TOP 5 ACC_Heisman_Winners FROM (SELECT DISTINCT ACC_Heisman_Winners FROM ACC_SCHOOLS_VIEW)SUBQUERY ORDER BY ACC_Heisman_Winners DESC

--TOP 5 Graduation_Rate
SELECT TOP 5 Graduation_Rate FROM (SELECT DISTINCT Graduation_Rate FROM ACC_SCHOOLS_VIEW)SUBQUERY ORDER BY Graduation_Rate DESC

--Putting it all together
SELECT
	*
FROM
	ACC_SCHOOLS_VIEW
WHERE
	ACC_Championships IN
		(SELECT TOP 5 ACC_Championships FROM (SELECT DISTINCT ACC_Championships FROM ACC_SCHOOLS_VIEW)SUBQUERY ORDER BY ACC_Championships DESC)
	AND ACC_Heisman_Winners IN
		(SELECT TOP 5 ACC_Heisman_Winners FROM (SELECT DISTINCT ACC_Heisman_Winners FROM ACC_SCHOOLS_VIEW)SUBQUERY ORDER BY ACC_Heisman_Winners DESC)
	AND Graduation_Rate IN
		(SELECT TOP 5 Graduation_Rate FROM (SELECT DISTINCT Graduation_Rate FROM ACC_SCHOOLS_VIEW)SUBQUERY ORDER BY Graduation_Rate DESC)