CREATE OR ALTER VIEW ACC_SCHOOLS_VIEW AS
SELECT
	SCHOOL.SCHOOL_NAME School,
	SCHOOL.SCHOOL_CITY City,
	SCHOOL.SCHOOL_STATE State,
