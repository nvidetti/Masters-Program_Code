	SUM(SEASON.WINS) ACC_Wins,
	SUM(SEASON.LOSSES) ACC_Losses,
	SUM(ISNULL(SEASON.TIES,0)) ACC_Ties,
	CAST((SUM(SEASON.WINS) + (0.5*SUM(ISNULL(SEASON.TIES,0)))) / SUM(SEASON.WINS + SEASON.LOSSES + ISNULL(SEASON.TIES,0)) AS DECIMAL(4,3)) ACC_Win_Percentage,
	SUM(CASE SEASON.ACC_CHAMPION WHEN 1 THEN 1 WHEN 0 THEN 0 END) ACC_Championships,
	COUNT(DISTINCT CASE WHEN HEISMAN.ACC_SEASON_ID IS NOT NULL THEN HEISMAN.HEISMAN_WINNER_ID END) ACC_Heisman_Winners,
	COUNT(DISTINCT HEISMAN.HEISMAN_WINNER_ID) Total_Heisman_Trophy_Winners
FROM
	ACC_SCHOOL SCHOOL
LEFT JOIN
	ACC_SEASON SEASON
		ON SCHOOL.ACC_SCHOOL_ID = SEASON.ACC_SCHOOL_ID
LEFT JOIN
	HEISMAN_WINNER HEISMAN
		ON SCHOOL.ACC_SCHOOL_ID = HEISMAN.ACC_SCHOOL_ID
GROUP BY
	SCHOOL.SCHOOL_NAME,
	SCHOOL.SCHOOL_CITY,
	SCHOOL.SCHOOL_STATE,
	SCHOOL.GRADUATION_RATE
GO

