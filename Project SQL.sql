CREATE OR ALTER PROCEDURE HEISMAN_WINNER_ACC_SCHOOL_ID AS
BEGIN
	DECLARE @loop INT = 1
    DECLARE @loopend INT
	DECLARE @ACC_SCHOOL_ID INT
	DECLARE @HEISMAN_SCHOOL_NAME VARCHAR(50)
	SELECT @loopend = COUNT(*) FROM HEISMAN_WINNER
	WHILE @loop <= @loopend
	BEGIN
		SELECT 
			@ACC_SCHOOL_ID = ACCSCH.ACC_SCHOOL_ID,
			@HEISMAN_SCHOOL_NAME = HWIN.HEISMAN_SCHOOL_NAME
		FROM 
			ACC_SCHOOL ACCSCH
		INNER JOIN
			HEISMAN_WINNER HWIN
				ON ACCSCH.SCHOOL_NAME = HWIN.HEISMAN_SCHOOL_NAME
				AND HWIN.ACC_SCHOOL_ID IS NULL
		UPDATE HEISMAN_WINNER SET ACC_SCHOOL_ID = @ACC_SCHOOL_ID WHERE HEISMAN_SCHOOL_NAME = @HEISMAN_SCHOOL_NAME
		SELECT @loop = @loop + 1
	END
END
GO

